//create a while loop to print 5 rows of 5 asterisks
//each with the row number in front of the rows
public class AsteriskLoop
{
    public static void main(){
        int x = 1; // Sets x to 1
        //If x is less than or equal to 5 then print
        //number and add : ***** to it
        while( x <= 5){ 
            System.out.println( x + ": *****");
            x++;
        }
    }


}
